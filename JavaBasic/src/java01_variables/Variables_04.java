package java01_variables;

public class Variables_04 {
	public static void main(String[] args) {
		
		int data1;
		int data2 = 777;
		
		data1 = 666;

		int data3;
		
		int data4, data5 = 456, data6;
		
		int data7, data8; double data9;
		//int data, data8, double data9; 에러가 발생한다.
		//같은 자료형의 변수를 여러개 선언할 때 ,(콤마)로 나열한다.
		//각각의 변수에 선언과 동시에 초기화 가능
		
		//서로 다른 자료형의 변수를 ,(콤마)로 나열하듯 선언할 수 없다.
	}
}
