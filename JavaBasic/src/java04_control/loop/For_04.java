package java04_control.loop;

public class For_04 {
	public static void main(String[] args) {
		
		// "Hello"를 7번 출력하시오(for문 이용)
		
		int i;
		
		for(i=0;i<7;i++) { // i, 0~6, x7
			System.out.println("Hello");
		}
		// for(x7) {
		// "hello"
		// }
		// "Hello" 출력 코드
		// 반복문(for)으로 7번 반복
		
		// 반복횟수 : 7번
		// 반복구간 : 0~6
		
		// i변수가 0부터 시작해서 7보다 작은동안 1씩 증가
		
	}
}
